# This is Day_08
# Title: For Loop

name = "Samyam"
for i in name:
    print(i)

colors = ["Red", "Yellow", "Blue", "Green", "Black"]
for color in colors:
    print(color)
    for i in color:
        print(i)

# Range
for k in range (5):  # goes from 0 to 4
    print(k)

for i in range(1, 12, 2): # Here, 2 is the step.
    print(i)

